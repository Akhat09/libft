#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new;
	t_list	*list;

	if (!lst)
		return (NULL);
	list = f(lst);
	new = list;
	while (lst)
	{
		lst = lst->next;
		if (!(list->next = f(lst)))
			free(list->next);
		list = list->next;
	}
	return (new);
}
